// Creating a while loop with if-else Statement.

var i = 0;
while(true){
    if(i<=10){
        console.log(i);
        i = i + 1;
    }
    else{
        break;
    }
}
// Creating a do-while loop with if-else Statement.

var a=1;
do{
    if(a<=10){
        console.log(a);
        a = a + 1;
    }
    else{
        break;
    }
}while(true);