// Creating an Switch Statement.

var sign = "leo";
switch(sign){
    case "tarus" : 
    console.log("Full of Energy");
    break;   
    case "virgo" :
    console.log("Full of Emotions");
    break;
    case "leo" :
    console.log("Full of Possitive Energy");
    break;
    default :
    console.log("Please Entered a Valid Sign");
    break;
}